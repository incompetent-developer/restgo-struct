package restgostruct

// GetAllLetterRunes is get mix letter and number
func GetAllLetterRunes() []rune {
	return letterRunes
}

// GetUpperRunes is get upper case only
func GetUpperRunes() []rune {
	return upperRunes
}

// GetLowerRunes is get lower case only
func GetLowerRunes() []rune {
	return lowerRunes
}

// GetNumberRunes is get number only
func GetNumberRunes() []rune {
	return numberRunes
}

// GetOddNumberRunes is get odd number only
func GetOddNumberRunes() []rune {
	return oddNumberRunes
}

// GetEvenNumberRunes is get even number only
func GetEvenNumberRunes() []rune {
	return evenNumberRunes
}
